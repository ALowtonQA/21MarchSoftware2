package com.qa.day1;

public class Calculator {
	
	public static int add(int a, int b) {
		return a + b;
	}
	
	public static int mul(int a, int b) {
		return a * b;
	}
	
	public static int sub(int a, int b) {
		return a - b;
	}
	
	public static double div(int a, int b) {
		double x = a;
		double y = b;
		return x / y;
	}
	
}
